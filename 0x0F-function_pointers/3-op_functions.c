#include "3-calc.h"
/**
 * op_add - sums two numbers
 * @a: the first number
 * @b: the second number
 * Return: the sum
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - difference two numbers
 * @a: the first number
 * @b: the second number
 * Return: the diff
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - sums two numbers
 * @a: the first number
 * @b: the second number
 * Return: the mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two numbers
 * @a: the first number
 * @b: the second number
 * Return: the divide
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - sums two numbers
 * @a: the first number
 * @b: the second number
 * Return: the odd
 */
int op_mod(int a, int b)
{
	return (a % b);
}
